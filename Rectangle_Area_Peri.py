#Code for finding the area and perimeter of the Rectangle

l=int(input("Enter the length of the Rectangle: "))
b=int(input("Enter the breadth of the Rectangle: "))

#Finding Area
Area=l*b

#Finding Perimeter
Perimeter=2*(l+b)

print(f"The area of the Rectange is: {Area} (m.square) \nThe perimeter of rectangle is: {Perimeter} (meters)")